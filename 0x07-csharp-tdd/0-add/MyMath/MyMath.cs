using System;

namespace MyMath
{
    /// <summary>
    /// Class containing various math operations
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Adds 2 numbers together
        /// </summary>
        /// <returns> Sum of 2 given integers </returns>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
